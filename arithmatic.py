# TO TRY ALL ARITHMATIC OPERATERS IN PYTHON.

A=int(input("ENTER VALUE OF A :- "))
B=int(input("ENTER VALUE OF B :- "))

print("A+B IS ",A+B)
print("A-B IS ",A-B)
print("A*B IS ",A*B)
print("A/B IS ",A/B)
print("A%B IS ",A%B)
print("A//B IS ",A//B)
print("A**B IS ",A**B)

"""HERE A//B MEANS DIVISION WITHOUT ANY DECIMAL,
   AND A**B MEANS A TO THE POWER B."""